// ---------------------------------SWITCH WITHOUT BREAK-------------

/*
        Every switch statement runs
        let day=0
        switch(day){
            case 0:
                console.log('Sunday')
            case 1:
                console.log('Monday')
            case 2 :
                console.log('Tuesday')
            default :
            console.log('Error')
        }

*/

// ---------------------------------SWITCH WITH BREAK-------------


/*
            Only matches day will run and then breaks out of switch
            let day=0
            switch(day){
                case 0:
                    console.log('Sunday')
                    break
                case 1:
                    console.log('Monday')
                    break
                case 2 :
                    console.log('Tuesday')
                    break
                default :
                console.log('Error')
            }

*/